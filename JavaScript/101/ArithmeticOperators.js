/**
 * Arithmetic Operators
 * Left Op[Operators] Right
 * Operators
 * Add : Left Op[+] Right
 * Subtract : Left Op[-] Right
 * Multiply : Left Op[*] Right
 * Division : Left Op[/] Right
 * Remainder Of The Division : Left Op[%] Right
 */

// Add
let resualt = 2 + 5;
console.log(resualt);

// Subtract
resualt = 5 - 2;
console.log(resualt);

// Multiply
resualt = 5 * 2;
console.log(resualt);

// Division
resualt = 5 / 2;
console.log(resualt);

// Reminder Of The Division
resualt = 5 % 2;
console.log(resualt);

// Calculate Square
resualt = 5 ** 2;
console.log(resualt);
